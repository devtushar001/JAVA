package JavaCT.JavaCT19;

public class opOverloading {
    public static void main(String[] args) {
       System.out.println(4 + 3);
       System.out.println(4 + 3 + 5 ); // overloading
       System.out.println(4 + 3.5);
       System.out.println(3.6f + 3.2);
       System.out.println("10" + 20);
       System.out.println("10" +20.4);
    }

} 
