package JavaCT.JavaCT04;

public class ternaryoperator {
   public static void main(String[] args) {
      int a = 4, b = 3;
      int max = a > b ? a : b;
      System.out.println(max);

      // Another example

      int x = 7, y = 5;
      int min = x < y ? x : y;
      System.out.println(min);
   }
}
